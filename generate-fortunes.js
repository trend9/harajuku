// ユニークな選択を保証する関数
function getUniqueChoice(array, usedSet, maxAttempts = 100) {
    let attempts = 0;
    let choice;

    // すべて使い切った場合はリセット
    if (usedSet.size >= array.length) {
        usedSet.clear();
    }

    do {
        choice = randomChoice(array);
        attempts++;
        if (attempts > maxAttempts) {
            // 最大試行回数を超えた場合は、使用済みセットをリセット
            usedSet.clear();
        }
    } while (usedSet.has(choice) && attempts <= maxAttempts);

    usedSet.add(choice);
    return choice;
}

// 日付をフォーマット
function formatDate(date) {
    const year = date.getFullYear();
    const month = date.getMonth() + 1;
    const day = date.getDate();
    return `${year}年${month}月${day}日`;
}

